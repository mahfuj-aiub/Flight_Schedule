import java.lang.*;
public class Time{
    private int hour ;
    private int miniute;
    Time(){

    }
    public void setHour(int hour){
        this.hour=hour;
    }
    public void setMiniute(int miniute){
        this.miniute=miniute;
    }
    public int getHour(){
        return hour;
    }
    public int getMiniute(){
        return miniute;
    }
}